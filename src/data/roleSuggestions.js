// Function to get suggestions for a given role
export const getRoleSuggestions = (role) => {
  // Try exact match first
  if (roleSuggestions[role]) {
    return roleSuggestions[role];
  }
  
  // Try case-insensitive match
  const lowerRole = role.toLowerCase();
  for (const [key, suggestions] of Object.entries(roleSuggestions)) {
    if (key.toLowerCase() === lowerRole) {
      return suggestions;
    }
  }
  
  // Try partial match (role contains key or key contains role)
  for (const [key, suggestions] of Object.entries(roleSuggestions)) {
    if (key.toLowerCase().includes(lowerRole) || lowerRole.includes(key.toLowerCase())) {
      return suggestions;
    }
  }
  
  // Return empty array if no match found
  return [];
};

export default roleSuggestions;